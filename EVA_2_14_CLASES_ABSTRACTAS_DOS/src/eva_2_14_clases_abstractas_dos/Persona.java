package eva_2_14_clases_abstractas_dos;

/**
 *
 * @author Alan
 */
abstract public class Persona {
    
    private String nombre;
    private int edad;

    public Persona() {
    }

    public Persona(String nombre, int edad) {
        this.nombre = nombre;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    //MÉTODOS ABSTRACTOS PUEDEN COEXISTIR CON MÉTODOS NORMALES.
    public abstract void imprimirDatos();
    
}

